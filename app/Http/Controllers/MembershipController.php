        {
            $memberships = Membership::where('user_id', Auth::user()->id)
                                    ->where('status', 1)
                                    ->get();
            if (count($memberships)) $membership = $memberships[0]; else $membership = null;
        }
        else $membership = null;

        return view('membership', ['membership' => $membership]);
    }

